#include <unistd.h>

int	main(int a, char **b)
{
	int	i;

	(void)a;
	i = 0;
	while (b[0][i])
	{
		write(1, &b[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
